
/**
 * Create the text string of an item.
 * 
 * @param {{item_type:number,item_id:number,item_num:number}} item - item to create string of.
 * @returns {string} item name and amount
 */
module.exports = function create_item(item){
    var str = "";
    switch(item.item_type){
        case 1:
            str = mst_weapon[item.item_id];
            break;
        case 2:
            str = mst_guard[item.item_id];
            break;
        case 3:
            str = mst_summon_beast[item.item_id];
            break;
        case 4:
            str = mst_character[item.item_id];
            break;
      //case 5: never found
      //case 6: never found
        case 7:
        case 8:
        case 17:
        case 22:
        case 32:
            str = usable_items[item.item_id];
            break;
        case 9:
            str = mst_crystal[item.item_id];
            break;
        case 10:
            str = mst_summon_material[item.item_id];
            break;
        case 11:
            str = mst_equipment_material[item.item_id];
            break;
      //case 12: id 0 of 1000 in GL only
        case 13:
            str = gear_slots[item.item_id];
            break;
        case 14:
            str = providence_core[item.item_id];
            break;
        case 15:
            str = mst_stamp[item.item_id];
            break;
        case 16:
            str = mst_chara_saying[item.item_id];
            break;
        case 18:
            str = mst_lb_material[item.item_id];
            break;
      //case 19: never found
      //case 20: never found
        case 21:
            str = mst_guard[item.item_id];
            break;
        case 23:
            str = mst_medal[item.item_id];
            break;
      //case 25: never found
        case 24:
            str = mst_skin[item.item_id];
            break;
        case 26:
            str = mst_exp_item[item.item_id];
            break;
        case 27:
            str = mst_quartz[item.item_id];
            break;
      //case 28: never found
        case 29:
            str = mst_ex_skill[item.item_id];
            break;
      //case 30: never found
        case 31:
            str = mst_summonboard[item.item_id];
            break;
      //case 33: never found
        case 34:
            str = mst_hunts_permit_display[item.item_id];
            break;
        case 35:
            str = mst_chest[item.item_id];
            break;
        case 36:
            str = mst_status_icon[item.item_id];
            break;
        case 37:
            str = mst_link_stone[item.item_id];
            break;
        case 38:
            str = mst_treasure_key[item.item_id];
            break;
        case 39:
            str = mst_summon_decoration[item.item_id];
            break;
        case 40:
            str = mst_skip_ticket[item.item_id];
            break;
        case 41:
            str = mst_crystal_point[item.item_id];
            break;
      // nothing from here until 59
        case 59:
            str = mst_crystal_point[item.item_id];
            break;
        case 99:
            str = game_index[item.item_id];
            break;
        default:
            str = `Unknown type ${item.item_type}, id ${item.item_id}`
            break;
    }
    if(str == undefined){
        str = `Unknown id ${item.item_id} in type ${item.item_type}`
    }
    if(item.item_num != 1){
        str += ` x ${item.item_num}`
    }
    return str;
}

